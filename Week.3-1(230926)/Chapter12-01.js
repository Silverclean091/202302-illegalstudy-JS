
// 아래의 빈칸과 서로 차이점을 서술하시오.

function add(x, y) {    // x, y를 뭐라 하는가? : 매개변수, 인자, parameter
    return x + y;
}

add(2, 5)               // 들어가는 값에 대한 단어 : 인수, 전달인자, argument


/* 

인자 : 함수를 호출할 때 전달되는 값이 담기는 변수
인수 : 함수를 호출할 때 전달되는 값

인수(argument)는 고정되어있지 않고, 함수를 호출할 때마다 수시로 변하는 값이다.
즉, 함수가 호출될 때 전달되는 실제의 값이다.

인자(parameter)는 함수가 호출되며 전해지는 인수의 값이 담기게 되는 변수이다.
즉, 함수의 변수 선언 목록을 나타낸다.

*/
